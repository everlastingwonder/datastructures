 /* Initialize: initialize head and indices avail and {lastnode.	*/ 
 void Initialize(int *head, int *avail, int *lastnode) 
 { 
 	*head  = -1;	/* The list is empty.					*/ 
 	*avail = -1;	/* Stack of available nodes is empty.	*/ 
 	*lastnode = -1;	/* None of the positions has been used. */ 
 } 
